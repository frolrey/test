<?php
/*Дан некоторый массив с числами, например, вот такой:

[1, 2, 3, 4, 5, 6]
Слейте пары элементов вместе:

[12, 34, 56]*/ 
$z = range(1,6);
$count = 0;
$k = [];
for ($i=0; $i<count($z); $i++)
{
   $count++;
   if ($count % 2 == 0)
   {
      $k[]= strval($z[$i-1]).strval($z[$i]);
   }
}
echo "<pre>";
print_r($k);